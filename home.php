            Anime is a diverse medium with distinctive production methods that have adapted in response to emergent technologies.<br>
            It combines graphic art, characterization, cinematography, and other forms of imaginative and individualistic techniques.<br>
            Compared to Western animation, anime production generally focuses less on movement, and more on the detail
            of settings and use of "camera effects", such as panning, zooming, and angle shots.<br>
            Diverse art styles are used, and character proportions and features can be quite varied, with a common
            characteristic feature being large and emotive eyes.<br>
